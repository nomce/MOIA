public class Coup {
    private int valid;
    private int depart;
    private int arrivee;

    public int getValid() {
        return valid;
    }

    public void setValid(int valid) {
        this.valid = valid;
    }

    public int getDepart() {
        return depart;
    }

    public void setDepart(int depart) {
        this.depart = depart;
    }

    public int getArrivee() {
        return arrivee;
    }

    public void setArrivee(int arrivee) {
        this.arrivee = arrivee;
    }

}
